import Foundation
import UIKit

extension UIColor {

    static var isabelline: UIColor {
        return UIColor(red: 225.0 / 255.0, green: 225.0 / 255.0, blue: 225.0 / 255.0, alpha: 1.0) //#E6E6E6
    }
    
    static var lightIsabelline: UIColor {
        return UIColor(red: 240.0 / 255.0, green: 240.0 / 255.0, blue: 240.0 / 255.0, alpha: 1.0) //#F0F0F0
    }
    
    static var willpowerOrange: UIColor {
        return UIColor(red: 253.0 / 255.0, green: 88.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0) //#FD5800
    }
    
}
